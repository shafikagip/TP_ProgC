#include <stdio.h>

struct Couleur {
    unsigned char R, G, B, A;
};

int main() {
    struct Couleur tab[10] = {
        {0xef,0x78,0x12,0xff},{0x2c,0xc8,0x64,0xff},{0xff,0x00,0x00,0xff},
        {0x00,0xff,0x00,0xff},{0x00,0x00,0xff,0xff},{0xff,0xff,0x00,0xff},
        {0x00,0xff,0xff,0xff},{0xff,0x00,0xff,0xff},{0x80,0x80,0x80,0xff},{0x7f,0x7f,0x7f,0xff}
    };

    for(int i = 0; i < 10; i++)
        printf("Couleur %d : R=%d G=%d B=%d A=%d\n",
            i+1, tab[i].R, tab[i].G, tab[i].B, tab[i].A);

    return 0;
}
